//Add
module.exports.add = function (a, b) {
    return a + b;
};

//Sub
module.exports.sub = function (a, b) {
    return a - b;
};

//Mul
module.exports.mul = function (a, b) {
    return a * b;
};

//Div
module.exports.div = function (a, b) {
    return a / b;
};